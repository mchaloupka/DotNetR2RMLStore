namespace Slp.Evi.Storage.Common.Algebra
{
    /// <summary>
    /// Enumeration of comparison types
    /// </summary>
    public enum ComparisonTypes
    {
        /// <summary>
        /// &gt;
        /// </summary>
        GreaterThan,
        /// <summary>
        /// &gt;=
        /// </summary>
        GreaterOrEqualThan,
        /// <summary>
        /// &lt;
        /// </summary>
        LessThan,
        /// <summary>
        /// &lt;=
        /// </summary>
        LessOrEqualThan,
        /// <summary>
        /// =
        /// </summary>
        EqualTo,
        /// <summary>
        /// !=
        /// </summary>
        NotEqualTo
    }
}
